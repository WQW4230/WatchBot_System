//=============================================================
// 函数: void LCD_CONFIG_INIT(void)
// 描述: LCD设置初始化
//=============================================================
void LCD_CONFIG_INIT(void)
{
	lcdconfig.col=COL;
	lcdconfig.row=ROW;
	lcdconfig.col_pre=COL_Pre;
	lcdconfig.row_pre=ROW_Pre;
	#ifdef LCD_MCU8
	lcdconfig.lcd_interface=MCU8;
	#endif
	#ifdef LCD_MCU16
	lcdconfig.lcd_interface=MCU16;
	#endif
	#ifdef LCD_MCUH8
	lcdconfig.lcd_interface=MCUH8;
	#endif
	#ifdef LCD_SPI4Line
	lcdconfig.lcd_interface=SPI4Line;
	#endif
	#ifdef LCD_SPI3Line
	lcdconfig.lcd_interface=SPI3Line;
	#endif
	#ifdef LCD_SPI3Line2
	lcdconfig.lcd_interface=SPI3Line2;
	#endif
	#ifdef LCD_SPI4Line_CS_D8
	lcdconfig.lcd_interface=SPI4Line_CS_D8;
	#endif
	lcdconfig.delay_time=Delay_Time;
	#ifdef commdata16
	lcdconfig.is_commdata16=1;
	#else
	lcdconfig.is_commdata16=0;
	#endif
	#ifdef TEST_STAND_H9B
	lcdconfig.pic_bytes=(COL*ROW)*3;
	#else
	lcdconfig.pic_bytes=(COL*ROW)<<1;
	#endif
}
