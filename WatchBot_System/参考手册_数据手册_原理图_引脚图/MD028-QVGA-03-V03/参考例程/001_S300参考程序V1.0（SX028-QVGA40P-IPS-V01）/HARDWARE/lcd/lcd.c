void LCD_ShowChar(u16 x,u16 y,u8 num,u8 size,u8 mode)
{  							  
	u8 temp,t1,t;
	u16 y0=y;
	u8 csize=(size/8+((size%8)?1:0))*(size/2);		//得到字体一个字符对应点阵集所占的字节数	
 	num=num-' ';//得到偏移后的值（ASCII字库是从空格开始取模，所以-' '就是对应字符的字库）
	for(t=0;t<csize;t++)
	{   
		if(size==12)temp=asc2_1206[num][t]; 	 	//调用1206字体
		else if(size==16)temp=asc2_1608[num][t];	//调用1608字体
		else if(size==24)temp=asc2_2412[num][t];	//调用2412字体
		else return;								//没有的字库
		for(t1=0;t1<8;t1++)  //８个点
		{			    
			if(temp&0x80)LCD_Fast_DrawPoint(x,y,POINT_COLOR);
			else if(mode==0)LCD_Fast_DrawPoint(x,y,BACK_COLOR);
			temp<<=1;
			y++;       ////刷下一行 ，注意：PC2LCD2002取模方式设置：阴码+逐列式+顺向+C51格式　20180419
			if(y>=ROW)return;		//超区域了
			if((y-y0)==size)   //刷满一个字符高度时20180419
			{
				y=y0;  //回到第一行20180419
				x++;   //开刷下一列20180419
				if(x>=COL)return;	//超区域了
				break;
			}
		}  	 
	}  	    	   	 	  
}  
void LCD_Fast_DrawPoint(unsigned short x,unsigned short y,unsigned short color)
{	  
	LCD_SetCursor(x,y);	
	WriteOneDot(color);				
}
void LCD_ShowString(u16 x,u16 y,u16 width,u16 height,u8 size,u8 *p)
{ 
	u8 x0=x;
	width+=x;
	height+=y;
    while((*p<='~')&&(*p>=' '))//判断是不是非法字符!
    {       
        if(x>=width){x=x0;y+=size;}
        if(y>=height)break;//退出
        LCD_ShowChar(x,y,*p,size,0);
        x+=size/2;
        p++;
    }  
}
